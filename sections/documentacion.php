Breve descripción
Página web de un refugio de gatos callejeros con accesos:
Al público en general que muestra:
fichas de los gatos en adopción
secciones varias de información 
formulario de contacto
Por login al área administrativa que permite operaciones CRUD sobre:
tabla de gatos pensionados
tabla de voluntarios

Tecnologías
HTML5 
CSS3 
Javascript 
PHP
SQL

Librerías / Frameworks / Plugins / Scripts no propios
Boostrap 4, jQuery, Popper
